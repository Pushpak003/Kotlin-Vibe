
// In Kotlin, you can get input from the user using the readLine() function, which reads input from the standard input stream (usually the keyboard). Here's a simple example:

fun main() {
    // Prompt the user to enter their name
    println("Please enter your name:")
    
    // Read the input from the user
    val name = readLine()
    
    // Print a greeting message using the input
    println("Hello, $name! Welcome to Kotlin programming.")
}

// When you run this program, it will print "Please enter your name:", wait for you to type something and press Enter, and then it will print "Hello, [whatever you typed]! Welcome to Kotlin programming."